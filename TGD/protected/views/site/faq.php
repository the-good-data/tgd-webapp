                <li>
                  <em>What does becoming a Member of TheGoodData mean?</em>
                  <p>Members of the company have similar rights as owners have in any other company including: access company information, attend and vote in General Meetings, elect or be elected as company Directors among other rights. Rights and duties of Members are explained in the company Primary Rules and Secondary Rules.</p>
                </li>
                <li>
                  <em>How can I become a Member?</em>
                  <p>To become a Member you must have used the service in the past 3 months and be at least 16 years old. If you fulfill this criteria you can apply for Membership in the website under the “collaborate” button. You will be asked to accept the Primary Rules and Company Principles and, if your application is approved, you will have to buy one company share.</p>
                </li>
                <li>
                  <em>Do I need to pay for a share to become a Member?</em>
                  <p>The short answer is No. As an Industrial and Provident Society we are forced by law to ask our Members to own a fully paid share. In order to facilitate the process we have reduced the share price to the minimum possible (1 pence) and created a 0-interest loan agreement so you can buy that share without doing any payment. Whenever you end your Membership, that loan is cancelled against the transfer of the share ownership to TheGoodData. No payment either.</p>
                  <p>However, you are welcome to buy that 1p share paying with bitcoins or even supporting us with a donation from which we will deduct the 1p to give you your share.</p>
                </li>
                <li>
                  <em>Are there any Membership rules?</em>
                  <p>Main corporate rules are included in the Primary Rules agreed when becoming member. Day-to-day governance rules are included in this Secondary Rules. We encourage you to read both documents to find the answer for company related topics not covered by these FAQs. In case of unintended discrepancy, Primary and Secondary Rules prevail over FAQs or any other company material.</p>
                </li>
                <li>
                  <em>How can Membership be cancelled?</em>
                  <p>Member can decide to quit Membership at anytime, by communicating it to <a href="mailto:members@thegooddata.org">members@thegooddata.org</a>. Membership will also be cancelled due to lack of use of the service during 3 consecutive months or failure to hold one Membership share. Membership Committee can also decide to expulse the Member if he has acted against company interests or principles, if he has repeatedly misbehaved in company meetings or forums or if he has provided false information to the company. Membership Committee will be formed by company and Members designated people. </p>
                  <p>When Membership comes to an end for any of these reasons, the Member must sell back its share to the company at its nominal value.</p>
                </li>
              </ul>
