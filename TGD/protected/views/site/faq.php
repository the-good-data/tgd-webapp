            <div class="tab-pane active" id="company">
              <ul class="clearfix">
                <li>
                  <em>What is TheGoodData Mission?</em>
                  <p>We help people enjoy ownership of their data.</br>
                  We want to do it following these <?php echo CHtml::link('Company Principles','legal#principles');?>.</p>
                </li>
                <li>
                  <em>Why did you start the company?</em>
                  <p>We believe that data will be a primary source of wealth in the new digital economy, in much the same way that land or labour has been up to now. Despite the fact that most data is “produced” by individuals, we tend not to act as the owners of it because most of that data being is processed and traded by corporations without our knowledge or fair agreement.</p>
                  <p>At TheGoodData, we want to ensure that people are aware of the data they produce, that they can control who has access to it, and that they receive the maximum benefit if they decide to share it.</p>
                </li>
                <li>
                  <em>What are your starting business goals?</em>
                  <p>Our first objective is to ensure that people own their online data. This means helping them to control who has access to their browsing data and maximizing the social benefit of that data if they decide to anonymously trade part of it.</p>
                </li>
                <li>
                  <em>How do you make money?</em>
                  <p>We are currently monetizing our users attention time through advertising. Moreover we are frequently receiving funds from private donors as you may also be here</p>
                  <p>Once we manage a higher volume of users, we expect to attract friendly sponsors and buyers interested in accessing anonymized data with people's consent.</p>
                </li>
                <li>
                  <em>Why are you organized as a cooperative?</em>
                  <p>Because our company mission is so ambitious, we believe that it will only be truly achievable with the active collaboration of many people. We also believe that this need for active involvement is best enabled through a platform which facilitates transparency, participation and fairness.</p>
                  <p>For this reason, TheGoodData has taken the legal form of an Industrial and Provident Society in the UK. It is a cooperative, in which users of the service become company owners if they should choose to do so (‘Member’ is the legal term). In this way, important decisions including the use of benefits and the property of key assets such as the data gathered, remain in the hands of our Members.</p>
                </li>
                <li>
                  <em>Does TheGoodData have any financial investors?</em>
                  <p>The only owners of TheGoodData are its Members. In the future, and only upon the approval of Members, some investors may be considered as financial partners if they are able to boost growth and help us to successfully accomplish our Mission. These investors would have limited rights to ensure that Members remain in control of the company.</p>
                </li>
              </ul>
            </div>
            <div class="tab-pane" id="service">
              <ul class="clearfix">
                <li>
                  <em>What is TheGoodData service?</em>
                  <p>TheGoodData service as it exists today is an extension that helps you to control the browsing data that you produce, and to use this data for a good cause. It stops major third parties from tracking the webpages you visit and, if you agree to do so, it will trade non sensitive anonymous browsing data with brokers and reinvest 100% of the money earned into social good projects and in improving TheGoodData service.</p>
                </li>
                <li>
                  <em>Which browsers does TheGoodData support?</em>
                  <p>Right now, TheGoodData is only available for Chrome. We would welcome your help in developing the open source extension for other browsers, and in return you can enjoy the benefits of our <a href="/coders">Collaborator’s Program</a>.</p>
                </li>
                <li>
                  <em>How do I use TheGoodData service?</em>
                  <p>Once installed, TheGoodData runs automatically in the background. You can click on TheGoodData icon in your toolbar to view, block, or unblock third-party requests or to otherwise change the default settings. You can view more detailed information about your data usage and learn more about TheGoodData by visiting our website.</p>
                </li>
                <li>
                  <em>How does TheGoodData work?</em>
                  <p>TheGoodData uses opensource disconnect.me technology to block data requests from over two thousand third-party tracking companies, all without degrading the functionality of the sites that you visit. TheGoodData allows you to easily unblock a third party that you trust by clicking the extension icon and then clicking on the name of the third party you want to allow, or to simply allow all tracking by a particular website if you wish.</p>
                  <p>In addition to blocking browsing threats -if you give permission- TheGoodData anonymizes and stores your browsing history and search queries for a limited amount of time (3 months). This valuable information will help us build reports identifying good and evil players in terms of online tracking.</p>
                </li>
                <li>
                  <em>What kind of projects does TheGoodData fund with my data?</em>
                  <p>20% of the money earned is shared with the collaborators that build TheGoodData. A small amount is used as well to pay running costs (e.g. servers). The remaining earnings are fully reinvested in microloan credits to alleviate poverty in developing countries. This investment is done via Zidisha.</p>
                  <p>Our reasons for choosing Zidisha are threefold:</p>
                  <ul class="clearfix">
                    <li><p>We believe that data can be a powerful force for good, not only for our users and Members, but also for humanity.</p> </li>
                    <li><p>We embody and practise the same principles of collaboration, transparency and social good that Zidisha does.</p> </li>
                    <li><p>Zidisha’s microloans help to create opportunities where there are so few, doing good without eroding our Members’ assets.</p></li>
                  </ul>
                </li>
                <li>
                  <em>Why are some icons red, green and orange?</em>
                  <p>Red = blocked. Green = allowed. Orange = traded with your permission.</p>
                </li>
                <li>
                  <em>Why are the Content threats in green by default?</em>
                  <p>By default, we do not block third parties that are critical to the delivery of content to a web page. However if you would like to block this third-party content on the site you’re visiting, simply click on the threat and it will change to red, indicating that it is now being blocked.</p>
                </li>
                <li>
                  <em>What are the “Whitelist this site” and “Allow social networks” extension features?</em>
                  <p>These features provide you with the ability to unblock some or all third parties on the site you are visiting with a single click. This can be especially useful if you notice that certain elements of a site are not functioning the way you would normally expect them to.</p>
                </li>
                <li>
                  <em>What are the “Store” and “Share” extension features?</em>
                  <p>If you don’t want to store your browsing history or your non-sensitive queries in our servers you can change these settings in the website at any time. You can also decide to sell none of your search data. Irrespective of your settings, TheGoodData’s threat detection service will always be available to you.</p>
                </li>
                <li id="search_query_trade">
                  <em>Why is the extension not storing my search queries?</em>
                  <p>TheGoodData extension will only store queries when the following criteria are met:</p>
                  <ul class="clearfix">
                    <li>
                      <p>You have performed the query in the Chrome browser, with the extension enabled, and you are using either the Google, Bing or Yahoo search engines.</p>
                    </li>
                    <li>
                      <p>Your query does not contain any sensitive terms. We err on the side of caution when it comes to protecting your data, so we may discard queries where there is any risk that they contain sensitive terms.</p>
                    </li>
                    <li>
                      <p>Your query must be written in English. At this early stage we are assuming the query language based on your browser’s default language. It might well be that you have written your query in English but we have discarded it because your browser’s default language isn’t English. We will aim to work across several languages in the future.</p>
                    </li>
                  </ul>
                </li>
                <li>
                  <em>Why do you store information on TheGoodData servers?</em>
                  <p>TheGoodData aims to harvest, process and trade on your behalf as much or as little of your data as you allow us to. We are currently trading search data, but we expect to trade data based on browsing history soon. This anonymous information and stored on our servers using the safest security standards. You can change permission levels, and access or delete any of your stored information at any time.</p>
                </li>
                <li>
                  <em>Will TheGoodData work if I have multiple blocking extensions installed in my browser?</em>
                  <p>In order for TheGoodData to work properly in Chrome, it must be the most recent blocking extension installed.</p>
                </li>
                <li>
                  <em>How do I disable or uninstall TheGoodData?</em>
                  <p>If you choose to leave, we’ll be sad to see you go. Please let us know your reasons for unsubscribing by dropping us an email at <a href="mailto:info@thegooddata.org">info@thegooddata.org</a>. This will help us to improve our service.</p>
                  <p>In Chrome, click on the “Window” menu button in the top navigation bar. and then choose the Extensions item. Uncheck the ‘Enabled’ button to disable, or click the trash icon to uninstall.</p>
                </li>
              </ul>
