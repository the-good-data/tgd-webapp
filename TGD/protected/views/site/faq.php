                <li>
                  <em>How can I collaborate with TheGoodData?</em>
                  <p>The easiest and most effective way to collaborate with TheGoodData is by using the service and asking others to do so.</p>
                  <p>Besides that, you can support us as much as you want. TheGoodData is a collaborative company and is open to everyone. We have built a platform at <a href="https://collaborate.thegooddata.org/">https://collaborate.thegooddata.org/</a> that will facilitate your access to any relevant information about the company, and aso provides you with an opportunity to comment on or lead a discussion about existing or new lines of business. We would love to see your participation there.</p>
                </li>
                <li>
                  <em>Can I contact company Directors directly?</em>
                  <p>Transparency also means direct communication. If you have any ideas for improvement or expertise that can be useful to us, you can email or make a hangout call to our company Director Marcos Menendez at <a href="mailto:marcos@thegooddata.org">marcos@thegooddata.org</a></p>
                </li>
                <li>
                  <em>Are coders rewarded?</em>
                  <p>In case you want to build any kind of new service functionality by leveraging on our assets, you are welcome to join our Coder’s Program. One sixth of company's revenues will be reserved for all coders during the six years that follow their contributions.</p>
                  <p>Please contact any of our company Directors or write to <a href="mailto:coders@thegooddata.org">coders@thegooddata.org</a> describing your idea, the expected level of effort needed and a CV outline to receive a strategic priority evaluation. </p>
                </li>
                <li id="rewards_calculation">
                  <em>How are rewards calculated?</em>
                  <p>In a given year, code released during the previous six years will be rewarded with one sixth of company revenues. They will all be rewarded proportionally considering:</p>
                  <ul class="clearfix">
                    <li>
                      <p>Their strategic importance: value provided by TheGoodData prior to starting developments</p>
                    </li>
                    <li>
                      <p>The level of effort: preferably measured preferably by lines of code or alternatively the estimated number of hours dedicated.</p>
                    </li>
                    <li>
                      <p>The output quality evaluated by other coders participating in the program</p>
                    </li>
                  </ul>
