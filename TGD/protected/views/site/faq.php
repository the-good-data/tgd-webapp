            <div class="tab-pane" id="membership">
              <ul class="clearfix">
                <li>
                  <em>What does becoming a Member of TheGoodData mean?</em>
                  <p>Members of the company have similar rights as owners have in any other company, including access to company information, attending and voting in General Meetings, and electing or being elected as company Directors among other rights. The rights and duties of Members are explained in the company's Primary and Secondary Rules.</p>
                </li>
                <li>
                  <em>How can I become a Member?</em>
                  <p>To become a Member you must have used the service in the past 3 months and be at least 16 years old. If you fulfill these criteria you can apply for Membership via the website under the “collaborate” button at the top of the homepage. You will be asked to accept the Primary Rules and Company Principles and, if your application is approved, you will have to buy one company share.</p>
                </li>
                <li>
                  <em>Do I need to pay for a share to become a Member?</em>
                  <p>The short answer is no. As TheGoodData is registered as an Industrial and Provident Society, we are forced by law to ask our Members to own a fully paid share. In order to facilitate the process we have reduced the share price to the minimum possible (1 pence) and created a 0-interest loan agreement so you can buy that share without making any payment. If you ever terminate your Membership, this loan is cancelled against the transfer of the share ownership to TheGoodData. No payment is required for this either.</p>
                  <p>However, you are welcome to buy the 1p share paying with bitcoins or even supporting us with a donation from which we will deduct the 1p to give you your share.</p>
                </li>
                <li>
                  <em>Are there any Membership rules?</em>
                  <p>Main corporate rules are included in the Primary Rules which are agreed when becoming a member. Day-to-day governance rules are included in the Secondary Rules. We encourage you to read both documents to find answers to company related topics not covered by these FAQs. In the case of any unintended discrepancies, the Primary and Secondary Rules will prevail over the FAQs or any other company material.</p>
                </li>
                <li>
                  <em>How can Membership be cancelled?</em>
                  <p>A Member can decide to terminate their Membership at anytime, by communicating their wish to do so to <a href="mailto:members@thegooddata.org">members@thegooddata.org</a>. Membership will also be cancelled due to a lack of use of the service during 3 consecutive months or due to the failure to hold one Membership share. The Membership Committee can also decide to expel a Member if they haves acted against company interests or principles, if they have repeatedly misbehaved in company meetings or forums or if they have provided false information to the company. Membership Committee will be formed by company Members designated individuals. </p>
                  <p>If Membership comes to an end for any of these reasons, the Member must sell back their share to the company at its nominal value.</p>
                </li>
              </ul>
