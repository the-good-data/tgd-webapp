            <div class="tab-pane active" id="company">
              <ul class="clearfix">
                <li>
                  <em>What is TheGoodData Mission?</em>
                  <p>We help people to enjoy the ownership of their data.</p>
                </li>
                <li>
                  <em>Why did you start the company?</em>
                  <p>We believe that data will be the main source of wealth in the new economy in the same way that land or labour had been in the past. Most of this data is being “produced” by individuals; however they do not act as owners, since most of that data is processed and traded by corporations without Customers’ knowledge or lacking a fair agreement.</p>
                  <p>We want to help people to be aware of the data they produce, to control who has access to it and to ensure that, when people decide to share their data, they receive the maximum benefit out of doing so. In this way Customers will not only be wealthier, will also improve their relationships with corporations who will not longer treat them as a product or as captive consumers.</p>
                </li>
                <li>
                  <em>What are your starting business goals?</em>
                  <p>Our first objective is to help people to effectively own their browsing data. This means helping them control who has access to their data and maximizing the social benefit of said data if users decide to anonymously trade part of it.</p>
                </li>
                <li>
                  <em>How do you make money?</em>
                  <p>TheGoodData aims to be self-financed by the revenues generated by the data you allow us to trade, in addition to donations from our supporters. Access to our services is free, irrespective of whether you decide to trade part of your data. We believe that you have to make this decision freely.</p>
                </li>
                <li>
                  <em>How do I know you are not sharing any sensitive or personally identifiable information?</em>
                  <p>In short, the GitHub logos and Octocat represent only GitHub and should not be used to represent your products. You may also read the official legal terms, if you’re into that.</p>
                </li>
                <li>
                  <em>Why are you organized as a cooperative?</em>
                  <p>Our company mission is extremely ambitious. It will only be achievable with the active collaboration of large amounts of people. We believe that this involvement can  be enabled through a platform that facilitates transparency, participation and fairness. </p>
                  <p>For this reason, TheGoodData has taken the legal form of an Industrial and Provident Society in the UK. It is a for-profit cooperative, in which users of the service become company owners if they apply for it (Members is the legal term). In that way important decisions including the use of benefits and the property of key assets such as  the data gathered, remain in the hands of our Members.</p>
                </li>
                <li>
                  <em>Does TheGoodData have any financial investors?</em>
                  <p>The only owners of TheGoodData at this point are its Members. In the future, only upon the approval of Members, some financial investors might be  brought in to boost growth and ensure the successful accomplishment of our mission. These investors would have some rightsq limitations in order to ensure that Members remain in control of the company.</p>
                </li>
              </ul>
            </div>
            <div class="tab-pane" id="service">
              <ul class="clearfix">
                <li>
                  <em>What is TheGoodData service</em>
                  <p>TheGoodData service as it exists today is an extension that helps you to control the browsing data that you produce, and use it for a good cause, should you wish to. It stops major third parties from tracking the webpages you viit and only if you agree to do so, trades non sensitive anonymised search data with brokers, reinvesting 100% of the money earned in Social Good projects and in improving TheGoodData service.</p>
                </li>
                <li>
                  <em>Which browsers does TheGoodData support?</em>
                  <p>TheGoodData is only available for Chrome at this point. We encourge you to help us develop the open source extension for other browsers enjoying the benefits of our Coder’s Program.</p>
                </li>
                <li>
                  <em>How do I use TheGoodData service?</em>
                  <p>TheGoodData runs automatically in the background, so you do not have to do anything after installing the extension. You can click TheGoodData icon in your toolbar to view, block, or unblock third-party requests or to otherwise change the default settings. You can also view more detailed information about threats that have been blocked, as well as company achievements  by visiting our website.</p>
                </li>
                <li>
                  <em>How does TheGoodData work?</em>
                  <p>TheGoodData uses opensource Disconnect.me technology to block data requests from over 2000 third party tracking companies without degrading the functionality of the sites that you visit. You can easily unblock a third party that you trust by clicking TheGoodData icon and then clicking on the name of the third party you want to allow, under the “Evil Data” area or simply allowing all tracking of a particular website if you feel that the extension is affecting your experience.</p>
                  <p>Besides blocking browsing threats, TheGoodData captures your search queries and, if you allow it, sells anonymized data after discarding queries that may contain sensitive terms (political, sexual, religious or health related). This data is sold to search retargeting players enabling them to display ads related to your searches. Thanks to TheGoodData these players can only do it with your permission and by paying a fee to TheGoodData in exchange. 100% of the money earned  thanks to your collaboration is then reinvested in Social Good projects and in improving TheGoodData service.</p>
                </li>
                <li>
                  <em>What kind of projects does TheGoodData fund with my data?</em>
                  <p>50% of the money collected is reinvested in microloan credits to alleviate poverty in developing countries. This investment is done via Zidisha.</p>
                  <p>The reason for choosing Zidisha is threefold:</p>
                  <ul class="clearfix">
                    <li><p>we believe that our data can be Good not only for our users and Members, but also for humanity</p> </li>
                    <li><p>we support the same principles of collaboration, transparency and Social Good that Zidisha does</p> </li>
                    <li><p>these microloans help to create opportunities where there are so few and does not erode our Members assets, since most of these loans are usually paid back by lenders.</p></li>
                  </ul>
                </li>
                <li>
                  <em>Why are some icons red, green and orange?</em>
                  <p>Red = blocked. Green = allowed. Orange = traded with your permission.</p>
                </li>
                <li>
                  <em>Why are the Content threats in green by default?</em>
                  <p>By default, we do not block third parties that are critical to the delivery of content to a web page. However if you would like to block this third partie content on the site you’re visiting, simply click these threats and they will change to red, indicating that they are being blocked.</p>
                </li>
                <li>
                  <em>What are the “Whitelist this site” and “Allow social networks” extension features?</em>
                  <p>These features provides you with the ability to unblock some or all third parties on the site you are visiting with a single click. This can be especially useful if you ever notice that certain elements of a site are not functioning the way they normally do.</p>
                </li>
                <li>
                  <em>How are the different user levels calculated?</em>
                  <p>User levels are based on your degree of collaboration with TheGoodData during the last 30 days. The more data you let us trade for a good cause and the more you are involved in TheGoodData company, the higher level you will achieve:</p>
                  <ul class="clearfix">
                    <li>
                      <p>Apprentice: if the user is among the 5% of people that that have contributed with the least amount of data items</p>
                    </li>
                    <li>
                      <p>Journeyman: If the user has contributed with more data items than Apprentice users but has not yet become a Member of TheGoodData</p>
                    </li>
                    <li>
                      <p>Owner: If the user has become a Member of TheGoodData </p>
                    </li>
                    <li>
                      <p>Expert: If the Member is among the top 20% of data contributors</p>
                    </li>
                    <li>
                      <p>Collaborator: If the Member has participated in the online collaboration platform during the last month </p>
                    </li>
                  </ul>
                </li>
                <li>
                  <em>What are the “Store” and “Share” extension features?</em>
                  <p>In case you don’t want to store your browsing history and your non-sensitive queries in our servers you can change this setting in the extension at any time. You can also decide not to sell any of your search data. No matter what you decide concerning these two settings, you will always enjoy TheGoodData threat detection service.</p>
                </li>
                <li id="search_query_trade">
                  <em>Why is the extension not trading my the search queries?</em>
                  <p>TheGoodData extension only trade queries if the following criteria are met: </p>
                  <ul class="clearfix">
                    <li>
                      <p>You have performed the query in the Chrome browser, with the extension enabled, and using either Google, Bing or Yahoo search engines.</p>
                    </li>
                    <li>
                      <p>The query does not contain any sensitive terms. To the sake of caution we may discard queries that pose a minor risk of containing sensitive terms.</p>
                    </li>
                    <li>
                      <p>The query is written in English. At this early stage we are guessing the query language based on browser default language. It might well be that you have written your query in English but we have discarded it because your browser language is a different one. We will improve this feature in the future.</p>
                    </li>
                  </ul>
                </li>
                <li>
                  <em>Why do you store information on TheGoodData servers?</em>
                  <p>TheGoodData aims to harvest, process and trade on your behalf as much data as you allow us to. We are currently trading search data, but we expect to trade data based on browsing history soon. This information is anonymized and stored on our servers following demanding security standards. You can at all times change this permission, access or delete any information stored about you.</p>
                </li>
                <li>
                  <em>Will TheGoodData work if I have multiple blocking extensions installed in my browser?</em>
                  <p>In order for TheGoodData to work in Chrome, it must be the most recent blocking extension installed.</p>
                </li>
                <li>
                  <em>How do I disable or uninstall TheGoodData?</em>
                  <p>First off, we are sad to see you go. If you have a minute after following the instructions below, please tell us your reason for unsuscribing at info@thegooddata.org as it will help us to improve our service</p>
                  <p>In Chrome, please open the Window menu and then choose the Extensions item. Uncheck the Enabled button to disable or click the trash icon to uninstall.</p>
                </li>
              </ul>
