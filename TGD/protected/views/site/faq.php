              <ul class="clearfix">
                <li>
                  <em>What is TheGoodData Mission?</em>
                  <p>We help people to enjoy the ownership of their data.</p>
                </li>
                <li>
                  <em>Why did you start the company?</em>
                  <p>We believe that data will be the main source of wealth of the new economy in the same way that land or labour were in the past. Most of this data is being “produced” by individuals, however they are not acting as owners, since most of it is processed and traded by Corporations without Customers’ knowledge or lacking a fair agreement.</p>
                  <p>We want to help people to be aware of the data they produce, to control who can access to it and to ensure that, if people decide to share their data, they get the maximum benefit out of it. In that way Customers will not only be wealthier, but above all, they will improve their relationships with Corporations that will not longer treat them as a product or as captive consumers.</p>
                </li>
                <li>
                  <em>What are your first business goals?</em>
                  <p>Our first objective is to help people to effectively own their browsing data. That means helping them control who has access to their data and maximise the social benefit of that data if users decide to anonymously trade part of it.</p>
                </li>
                <li>
                  <em>How do you make money?</em>
                  <p>TheGoodData aims to be self-financed by the revenues made with the data you allow us to trade, plus the donations of our supporters. Access to our services is free and irrespective of your decision to trade or not some of your data. We believe that you have to take that decision freely.</p>
                </li>
                <li>
                  <em>How do I know you are not sharing any sensitive or personally identifiable information?</em>
                  <p>In short, the GitHub logos and Octocat represent only GitHub and should not be used to represent your products. You may also read the official legal terms, if you’re into that.</p>
                </li>
                <li>
                  <em>Why are you organized as a cooperative?</em>
                  <p>Our company mission is enormously ambitious. It will only be achievable with the active collaboration of large amounts of people. We believe that this involvement can only be achievable through a platform that facilitates transparency, participation and fairness. </p>
                  <p>That’s the reason why TheGoodData has taken the legal form of an Industrial and Provident Society in the UK. Its a for-profit cooperative where users of the service become company owners if they apply for it (Members is the legal term). In that way important decisions including the use of benefits and the property of key assets like the data gathered, remain in the hands of our Members.</p>
                </li>
                <li>
                  <em>Does TheGoodData have any financial investor?</em>
                  <p>The only owners of TheGoodData at this point are its Members. In the future, only upon Members approval, there might be some financial investors brought in to boost growth and ensure the successful accomplishment of our Mission. These investors would have some right limitations to ensure that Members remain on the driving seat of the company.</p>
                </li>
              </ul>
            </div>
            <div class="tab-pane" id="profile">
              <ul class="clearfix">
                <li>
                  <em>What is TheGoodData service</em>
                  <p>TheGoodData service as it stands today is an extension that helps you control the browsing data that you produce, and use it for a good cause if you want to. It stops major third parties from tracking the webpages you go to and, only if you agree, trades non sensitive anonymised search data with brokers, reinvesting 100% of the money earned in Social Good projects and in improving TheGoodData service.</p>
                </li>
                <li>
                  <em>Which browsers does TheGoodData support?</em>
                  <p>TheGoodData is available for Chrome only at this point. We invite you to help us develop the open source extension for other browsers enjoying the benefits of our Coder’s Program.</p>
                </li>
                <li>
                  <em>How do I use TheGoodData service?</em>
                  <p>TheGoodData runs automatically in the background, so you don’t have to do anything after installing. You can click TheGoodData icon in your toolbar to view, block, or unblock third-party requests or to otherwise change the defaults. You can also view more detailed information about threats blocked, company achievements and data gathered, by visiting our website.</p>
                </li>
                <li>
                  <em>How does TheGoodData work?</em>
                  <p>TheGoodData uses opensource Disconnect.me technology to block data requests from over 2000 third party tracking companies without degrading the functionality of the sites you visit. You can easily unblock a third party that you trust by clicking TheGoodData icon and then clicking on the name of the third party you want to allow, under the “Evil Data” area or simply allowing all tracking of a particular website if you feel that the extension is affecting your experience.</p>
                  <p>Besides blocking browsing threats, TheGoodData captures your search queries and, if you allow it, sells anonymised data after discarding queries that may contain sensitive terms (political, sexual, religious or health related). That data is sold to search retargeting players so they can latter display ads related to your searches. Thanks to TheGoodData these players will only operate with your permission and paying a fee to TheGoodData in exchange. 100% of the money earned with thanks to your collaboration is then reinvested in Social Good projects and in improving TheGoodData service.</p>
                </li>
                <li>
                  <em>What kind of projects does TheGoodData found with my data?</em>
                  <p>50% of the money collected is reinvested in microloan credits to alleviate poverty in developing countries. This investment is done via Kiva, the online leader of online microloans.</p>
                  <p>The reason for choosing Kiva is threefold:</p>
                  <ul class="clearfix">
                    <li><p>we believe that our data can be Good not just for our users or Members, but for humanity,</p> </li>
                    <li><p>we support the same principles of collaboration, transparency and Social Good that Kiva does,</p> </li>
                    <li><p>these microloans do not only help to create opportunities where there are so few, but do not erode our Members assets, since most of these loans are usually paid back by lenders.</p></li>
                  </ul>
                </li>
                <li>
                  <em>Why are some icons red and some icons green?</em>
                  <p>Red = blocked. Green = allowed.</p>
                </li>
                <li>
                  <em>Why are the Content threats in green by default?</em>
                  <p>By default, we do not block third parties that are critical to the delivery of content to a web page. However if you’d like to block content third parties on the site you’re visiting, simply click those threats and they will flip to green, indicating that they are being blocked.</p>
                </li>
                <li>
                  <em>What are the “Whitelist this site” and “Allow social networks” extension features?</em>
                  <p>These feature gives you the ability to unblock some or all third parties on the site you are visiting with a single click. This can be especially handy if you ever notice that certain elements of a site are not functioning the way they normally do.</p>
                </li>
                <li>
                  <em>How are the different user levels calculated?</em>
                  <p>User levels are based on your degree of collaboration to TheGoodData during the last 30 days. The more data you let us trade for a good cause and the more you are involved in TheGoodData company, the higher level you achieve:</p>
                  <ul class="clearfix">
                    <li>
                      <p>Apprentice: if the user is among the 5% of people that that have contributed with less data items</p>
                    </li>
                    <li>
                      <p>Journeyman: If the user has contributed with more data items than Apprentice users but has not yet become a Member of TheGoodData</p>
                    </li>
                    <li>
                      <p>Owner: If the user has become a Member of TheGoodData </p>
                    </li>
                    <li>
                      <p>Expert: If the Member is among the top 20% of data contributors</p>
                    </li>
                    <li>
                      <p>Collaborator: If the Member has participated in the online collaboration platform during the last month </p>
                    </li>
                  </ul>
                </li>
                <li>
                  <em>What are the “Store” and “Share” extension features?</em>
                  <p>In case you don’t want to store your browsing history and your non-sensitive queries in our servers you can change that setting in the extension at any time. You can decide as well not to sell any of your search data. No matter what you decide about these two settings, you will always enjoy TheGoodData threat detection service.</p>
                </li>
                <li id="search_query_trade">
                  <em>Why the extension does not trade the search queries that I’m doing?</em>
                  <p>TheGoodData extension only trade queries if the following criteria is met: </p>
                  <ul class="clearfix">
                    <li>
                      <p>You have performed the query in the Chrome browser, with the extension enabled, and using either Google, Bing or Yahoo search engine.</p>
                    </li>
                    <li>
                      <p>The query does not contain any sensitive term. To be on the safe side we may discard queries that have a minor risk of containing sensitive terms, but they are not.</p>
                    </li>
                    <li>
                      <p>The query is written in English. At this early stage we are guessing the query language based on browser default language. It might well be that you have written your query in English but we have discarded it because browser language is a different one. We will improve this feature in the future.</p>
                    </li>
                  </ul>
                </li>
                <li>
                  <em>Why do you store information in TheGoodData servers?</em>
                  <p>TheGoodData aims to harvest, process and trade on your behalf as much data as you allow us to. We are currently trading search data, but we expect to trade data based on browsing history soon. This information is anonymised and stored in our servers following most demanding security standards. You can at all times change this permission, access or delete any information stored about you.</p>
                </li>
                <li>
                  <em>Will TheGoodData work if I have multiple blocking extensions installed in my browser?</em>
                  <p>In order for TheGoodData to work on Chrome, it must be the most recent blocking extension installed.</p>
                </li>
                <li>
                  <em>How do I disable or uninstall TheGoodData?</em>
                  <p>First off, we're sad to see you go. If you have a minute after following the instructions below, please tell us what went wrong at info@thegooddata.org. It would help us to improve our service</p>
                  <p>On Chrome, please open the Window menu and then choose the Extensions item. Uncheck the Enabled button to disable or click the trash icon to uninstall.</p>
                </li>
              </ul>
